public class CreateCreatureSIAlgorithmException extends RuntimeException {

	private static final long serialVersionUID = -5756022372725427160L;

	public CreateCreatureSIAlgorithmException() {
	}

	public CreateCreatureSIAlgorithmException(final String arg0) {
		super(arg0);
	}

	public CreateCreatureSIAlgorithmException(final Throwable arg0) {
		super(arg0);
	}

	public CreateCreatureSIAlgorithmException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

	public CreateCreatureSIAlgorithmException(final String arg0, final Throwable arg1, final boolean arg2,
			final boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
